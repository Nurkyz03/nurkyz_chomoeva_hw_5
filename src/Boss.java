public class Boss {
    // Приватные поля класса Boss
    private int health;
    private int damage;
    private String defenseType;

    // Геттер для поля health
    public int getHealth() {
        return health;
    }

    // Сеттер для поля health
    public void setHealth(int health) {
        this.health = health;
    }

    // Геттер для поля damage
    public int getDamage() {
        return damage;
    }

    // Сеттер для поля damage
    public void setDamage(int damage) {
        this.damage = damage;
    }

    // Геттер для поля defenseType
    public String getDefenseType() {
        return defenseType;
    }

    // Сеттер для поля defenseType
    public void setDefenseType(String defenseType) {
        this.defenseType = defenseType;
    }
}
